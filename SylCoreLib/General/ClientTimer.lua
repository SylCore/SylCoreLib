ClientTimer = {
    timers = {},
}

function ClientTimer.HookEluna()
    -- Hook into Eluna’s update loop
    local timerFrame = CreateFrame("Frame")
    timerFrame:SetScript("OnUpdate", function(_, elapsed)
        for tag, t in pairs(Timer.timers) do
            t.elapsed = t.elapsed + elapsed
            if t.elapsed >= t.delay then
                t.callback()
                Timer.timers[tag] = nil -- remove after run
            end
        end
    end)
end

-- Schedules a timer with a tag, and a delay.
-- After the delay is done, it will call your "callback" function.
function ClientTimer.Schedule(tag, delay, callback)
    ClientTimer.Cancel(tag) -- cancel existing if needed
    local t = {
        elapsed = 0,
        delay = delay,
        callback = callback
    }

    ClientTimer.timers[tag] = t
end

-- Called with the tag to cancel a timer.
function ClientTimer.Cancel(tag)
    ClientTimer.timers[tag] = nil
end

-- Use this to add recurring timers.
function ClientTimer.Every(tag, interval, callback)
    ClientTimer.Schedule(tag, interval, function()
        callback()
        ClientTimer.Every(tag, interval, callback)
    end)
end

function ClientTimer:Init()
    print("[SylCoreLib.ClientTimer] Timer system ready.")
end

return ClientTimer;